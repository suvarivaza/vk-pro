<?php

namespace Service\Users;

class Controller_State_Admin_Referrers_Out_Settings extends Controller_State_Admin
{
    public function actionPrepare()
    {
        $this->_application->userPage = 'referrer';

        if (isset($this->_application->menu['users']['menu']['settings'])) {
            $this->_application->menu['users']['menu']['settings']['active'] = true;
        }

        return parent::actionPrepare();
    }

    public function actionGet()
    {
        $vars = [
            'settings' => Model_Config::GetReferrersSettings(),
        ];

        return $this->_response->setBody(\STPL::Fetch('admin/referrers/out/settings', $vars));
    }

    public function actionPost()
    {
        $action = $this->_request->post['action']->string();

        switch ($action) {
            case 'save':
                return $this->_save();
        }

        return parent::actionPost(); // TODO: Change the autogenerated stub
    }

    protected function _save()
    {
        $settings = Model_Config::GetReferrersSettings();
        $settings['out'] = $this->_request->post['out']->asArray();

        Model_Config::SetReferrersSettings($settings);

        return null;
    }
}
